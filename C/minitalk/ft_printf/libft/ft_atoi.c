int	ft_atoi(const char *str)
{
	int	s;
	int	c;

	c = 0;
	s = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			s = -s;
		str ++;
	}
	while (*str >= '0' && *str <= '9')
	{
		c = c * 10 + *str - 48;
		str++;
	}
	return (c * s);
}
